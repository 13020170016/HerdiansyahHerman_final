private static Connection buka_koneksi() {
        if (koneksi == null) {
            try {
                String url = "jdbc:mysql://localhost/petshop";
                String user = "root";
                String password = "";
                DriverManager.registerDriver(new com.mysql.jdbc.Driver());
                koneksi = DriverManager.getConnection(url, user, password);
            } catch (SQLException t) {
                System.out.println("Error membuat koneksi");
            }
        }
        return koneksi;
    }

    public void tambah() {
        koneksi = buka_koneksi();
        String sqlkode = "Insert into Data_Penitipan values ('"
                + kode_penitipan.getText()+ "','" 
                + kd_hwn.getSelectedItem()+ "','" 
                + Tanggal+"','"
                + lama_penitipan.getText()+"','"
                + kd_pkt.getSelectedItem()+"','"
                + Biaya_penitipan.getText()+"')";
              
                
        try {
            PreparedStatement p2 = (PreparedStatement) koneksi.prepareStatement(sqlkode);
            p2.executeUpdate();
            p2.close();
            ambil_data_tabel();
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Terjadi kesalahan " + ex.getMessage());
        }
    }
 public void hapus() {
        koneksi= buka_koneksi();
        String sqlkode = "Delete from Data_Penitipan " + "Where kd_penitipan='" + this.kode_penitipan.getText() + "'";
        try {
            PreparedStatement p2 = (PreparedStatement) koneksi.prepareStatement(sqlkode);
            p2.executeUpdate();
            p2.close();
            ambil_data_tabel();
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Terjadi kesalahan " + ex.getMessage());
        }

    }
 
