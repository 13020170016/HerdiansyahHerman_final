                +"kd_paket='"+this.kd_pkt.getSelectedItem()+"',"
                +"biaya_penitipan='"+this.Biaya_penitipan.getText()+"'"
                +"WHERE kd_penititpan='"+this.kode_penitipan.getText()+"'";
